a=int(input("Enter 1st number"))
b=int(input("Enter 2nd number"))
c=input("Press '+' for add \n '-' for subtract \n '*' for multiply \n '**' for exponentiation \n '/' for float division \n '//' for int division \n '%' for remainder \n ")
if c=="+":
    print(a+b)
if c=="-":
    print(a-b)
if c=="*":
    print(a*b)
if c=="**":
    print(a**b)
if c=="/":
    print(a/b)
if c=="//":
    print(a//b)
if c=="%":
    print(a%b)
